#include<bits/stdc++.h>
using namespace std;

using ll = long long;
using ull = unsigned long long;
#define YES cout << "YES\n"
#define NO cout << "NO\n"
#define nl "\n"
#define arrin(arr, n) for(ll i = 0; i < n; i++) cin>>arr[i]
#define arrout(arr, n) for(ll i = 0; i < n; i++) cout<<arr[i]<<" ";cout<<nl



int main()
{
    ios::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

    ll t;
    cin>>t;
    while (t--)
    {
        ll n;
        cin>>n;
        set<ll> s;
        for(ll i = 0; i< n;i++)
        {
            ll x;
            cin>>x;
            s.insert(x);
        
        }
        if(s.size()==n)
        {
            YES;
        }
        else
        {
            NO;
        }
        
    }
    
    return 0;
}




