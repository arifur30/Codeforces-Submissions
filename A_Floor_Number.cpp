#define nl "\n"
#define arrin(arr, n) for(ll i = 0; i < n; i++) cin>>arr[i]
#define arrout(arr, n) for(ll i = 0; i < n; i++) cout<<arr[i]<<" ";cout<<nl



int main()
{
    ios::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

    ll t;
    cin>>t;
    while (t--)
    {
        ll n, x;
        cin>>n>>x;

        if(n <= 2)
        {
            cout<<1<<nl;
            continue;
        }
        else{
            ll ans = ceil((n-2.0) / x);
            cout<<ans+1<<nl;
        }
    }
    
    return 0;
}




